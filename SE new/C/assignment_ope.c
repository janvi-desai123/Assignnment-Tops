#include<stdio.h>
int main()
{	
	//Assignment operators are used to assign values to variables.
	int x,y;
	x=10;// the assignment operator (=) to assign the value 10 to a variable called x
	y=15;
	x+=5;//The addition assignment operator (+=) adds a value to a variable
	x+=y;//x=x+y
	x-=3;//x=x-3
	x*=5;//x=x*5
	x/=3;//x=x/3
	x%=2;//x=x%2
	printf("%d",x);
	
	
	
	return 0;
}
